		<h3>What is meditation?</h3>

		<p>
			At its core, <strong
				>meditation is all about observing the movements of our
				awareness</strong
			>. It's about being present and watching your thoughts and feelings
			with kindness and clarity, rather than trying to control or change
			them.
		</p>

		<p>
			It's important to understand that not every meditation will feel
			blissful, and that's okay. The goal is to welcome each moment with
			kindness, shifting your focus from what you're experiencing to how
			you relate to those experiences.
		</p>

		<p>
			Throughout DIPP, we hope that you'll discover how to embrace
			challenges as opportunities for growth, meeting all experiences with
			curiosity and compassion. Through meditation, you'll learn to
			observe your inner world with acceptance and kindness – a skill that
			may help you during your psychedelic experience.
		</p>

		<h3>Posture and attitude</h3>

		<p>
			Before starting any meditation session, it's important to find a
			comfortable place to sit, either on a chair or a cushion on the
			floor. Make sure your back is straight but relaxed, as if a gentle
			force is supporting you. This posture helps you remain alert yet at
			ease.
		</p>

		<p>
			Before you start, take a moment to minimise distractions by
			silencing your phone and letting others know not to disturb you.
		</p>

		<p>
			A simple but powerful tip: <strong>add a small smile</strong>. This
			subtle change can make your meditation feel more relaxed and joyful.
			Give it a try now and observe how it feels.
		</p>

		<p>
			When you meditate, you can close your eyes to focus inward or keep
			them slightly open, gazing softly at a point in front of you. Choose
			what feels most natural.
		</p>

		<p>
			Remember, approach your practice with curiosity and openness,
			letting go of any expectations. <strong
				>There is no right or wrong way to meditate.</strong
			>
		</p>

		<h3>Metta (Loving-Kindness) Meditation</h3>

		<p>
			The core of this programme is the cultivation of metta. Often
			translated as 'loving-kindness', metta is an ancient Pali term and
			concept which means something like goodwill, friendliness and
			benevolence. <strong
				>Please note that while metta meditation is rooted in Buddhist
				practices, our programme applies this concept in a secular,
				universal manner</strong
			>, and throughout this 21-day programme, we will use terms like
			'loving-kindness' 'metta', 'friendliness', and 'goodwill'
			interchangeably.
		</p>

		<p>
			The practice itself involves cultivating, radiating and expanding a
			sense of friendly goodwill towards oneself and others. We will begin
			with shorter, simpler metta meditations and gradually deepen the
			practice over the course of the 21-day programme.
		</p>

		<p>
			In meditation, it's normal for minds to wander, so we use a homebase
			or mental anchor to return to when this happens. <strong
				>In DIPP, our anchor or homebase is the warm feeling of metta.</strong
			> We cultivate this feeling and gently return to it when distracted.
			This strengthens our ability to maintain kindness and goodwill, much
			like building muscle.
		</p>

		<p>
			The ultimate goal is that your 21 days of metta meditation practice
			will serve as a foundation, cultivating a positive, open, and
			compassionate mindset that can enhance your psychedelic journey and
			its integration. You'll learn to develop feelings of kindness and
			goodwill, starting with yourself and gradually expanding to all
			beings, aligning with the expanded states of consciousness often
			encountered in psychedelic experiences. This training aims to
			develop emotional resilience, improve self-compassion, and foster a
			sense of interconnectedness – qualities valuable in daily life as
			well as in preparing for and integrating psychedelic experiences.
		</p>

		<h3>Working with distractions</h3>

		<p>
			Many people new to meditation believe that getting distracted means
			they're doing it wrong and that experienced meditators don't face
			distractions. This is a misconception. In fact, <strong
				>distractions are an integral part of meditation.</strong
			> When you're practising, your mind will naturally and inevitably wander.
			It's actually at this point that the real meditation begins! Meditation
			is about observing these distractions and gently guiding your awareness
			back – to metta, in this case. By consistently practising meditation
			and learning to deal with distractions, you'll start to notice them sooner
			and gradually gain more insight into how your mind works. So distractions
			are absolutely part of meditation and not something to be frustrated
			about, but rather valuable opportunities to build awareness.
		</p>

		<p>
			Distractions can vary in intensity. Sometimes, they're minor – like
			hearing a distant noise, or having a fleeting thought cross your
			mind. In these cases, you can simply acknowledge the distraction and
			gently guide your attention back to metta. However, there will be
			times when you find yourself fully caught up in a train of thought,
			or suddenly realise, 'Oh, I'm supposed to be meditating!' When this
			happens – when you've become completely distracted or lost in
			thought – we use a technique called 'the Six Rs'. This technique
			helps you smoothly return to your meditation practice:
		</p>

		<ol>
			<li>
				<strong>Recognise</strong> that your mind has wandered.
			</li>
			<li>
				<strong>Release</strong> the distraction – just let it go.
			</li>
			<li>
				<strong>Relax</strong> your body and mind, letting go of any tension.
			</li>
			<li>
				<strong>Re-smile</strong>, bringing a gentle smile to your face.
			</li>
			<li>
				<strong>Return</strong> your focus to the feeling of metta.
			</li>
			<li>
				<strong>Repeat</strong> this process whenever you need to.
			</li>
		</ol>

		<p>
			The Six Rs are designed to be combined into a smooth, quick process,
			taking only a few seconds to complete. Think of them as gentle
			guidelines that help you return to your practice with kindness and
			ease.
		</p>

		<p>
			As you become more familiar with using the Six Rs, you might notice
			an unexpected sense of joy or lightness. This pleasant feeling often
			arises from releasing habitual thought patterns and tensions. The
			intensity of this experience can vary depending on how deep your
			meditation is and how relaxed you feel. With consistent practice,
			this initial joy may gradually deepen into a sense of tranquillity
			and contentment.
		</p>

		<p>
			Remember, getting distracted during meditation isn't a sign of
			failure – it's actually an opportunity to strengthen your meditation
			skills. Every time you notice your mind has wandered and gently
			bring it back, you're improving your ability to meditate. Over time,
			you'll likely find that working with distractions becomes more
			natural and effortless. So be patient with yourself, embrace the
			process, and enjoy your meditation journey!
		</p>

		<h3>Spectrum of Awareness</h3>

		<p>
			After working with the Six Rs for a bit, we will introduce another
			tool which you can use to take your meditation deeper. This tool is
			called the 'Spectrum of Awareness'. It's a bit like an extension of
			the recognition phase of the Six Rs, where you allow yourself to see
			where the mind has gone. It's here where we can go deeper into our
			meditation practice.
		</p>

		<p>
			As you progress in your meditation practice, you'll explore
			different layers of awareness using this spectrum, which includes
			five levels:
		</p>

		<ol>
			<li>
				<strong>Content</strong>: This is what you're actually thinking
				about – memories, plans or daydreams. It's the most obvious
				layer of thought.
			</li>
			<li>
				<strong>Process</strong>: This is how your mind is working – are
				you planning, worrying or imagining? This is the mental activity
				behind the content.
			</li>
			<li>
				<strong>Qualities</strong>: These are the emotional tones of
				your awareness. Is your mind feeling peaceful, agitated, or
				curious? This is about noticing the overall feel of your mental
				state.
			</li>
			<li>
				<strong>Awareness of Awareness</strong>: This is simply noticing
				that you're aware, without focusing on any specific thought or
				feeling. It's like watching your mind at work.
			</li>
			<li>
				<strong>Nothingness</strong>: This is a very deep state where
				awareness itself starts to fade. It might feel dreamlike, as if
				you're neither fully awake or asleep. This state is rare and
				usually only experienced in advanced practice.
			</li>
		</ol>

		<p>
			Remember, this isn't about analysing your feelings or thoughts. It's
			about gently noticing these different layers as you meditate. With
			practice, you may find your awareness naturally expanding through
			these levels, leading to a richer meditation experience.
		</p>

		<h3>Your Personal Journey</h3>

		<p>
			As you progress through this programme and complete your daily
			sessions remember that it's okay to move at your own pace. If
			expanding your circle of loving-kindness feels overwhelming, it's
			perfectly fine to focus on yourself and perhaps a close friend.
			Trust your comfort level and allow your approach to evolve naturally
			over time.
		</p>

		<p>
			Your experience of loving-kindness may change as you continue to
			meditate. The feeling might become quieter or take on a different
			quality of happiness. Allow these shifts to happen without trying to
			change anything. You're simply an observer on this journey, letting
			your feelings guide you to a deeper, calmer state.
		</p>

		<p>As your practice deepens, you might notice interesting changes:</p>

		<ul>
			<li>
				You may temporarily forget about parts of your body as feelings
				of love and kindness become clearer.
			</li>
			<li>
				The sensations of loving-kindness (metta) might evolve in
				unexpected ways.
			</li>
			<li>
				If feelings become overwhelming, it's okay to slow down and take
				things at your own pace.
			</li>
			<li>
				Should any discomfort arise (e.g., headaches, cramps etc) ease
				the intensity of your focus. Let your practice be guided by a
				gentle sense of kindness, accompanied by a soft smile.
			</li>
		</ul>

		<p>
			Embrace each stage of this journey with curiosity, remaining open to
			how your experience of loving-kindness may change and deepen over
			time.
		</p>

		<h2>Development of DIPP</h2>

		<p>
			DIPP was developed through a systematic, person-centred approach as
			described in McAlpine et al. (2024b). This process involved
			qualitative interviews with past psychedelic retreat participants
			and co-design workshops with individuals preparing for psilocybin
			experiences. The structure and content of DIPP are grounded in the
			four-factor model of psychedelic preparedness established by
			McAlpine et al. (2024a) in their development and validation of the
			Psychedelic Preparedness Scale (PPS). These four factors –
			Knowledge-Expectation, Psychophysical-Readiness,
			Intention-Preparation, and Safety-Planning – form the core modules
			of DIPP, ensuring a comprehensive approach to psychedelic
			preparation that is both theoretically sound and practically
			relevant to users' needs.
		</p>

		<p>
			The iterative development process, guided by participant feedback
			and expert consultation, resulted in a 21-day digital intervention
			that aims to enhance psychedelic preparedness across multiple
			domains. By incorporating insights from both clinical expertise and
			lived experience, DIPP represents a novel approach to standardising
			and optimising preparation for psychedelic experiences in various
			contexts.
		</p>

